'''
Знайти суму парних елементів масиву цілих чисел. Розмірність масиву -
20. Заповнення масиву здійснити випадковими числами від 100 до 200.
Гармідер Анастасія 122В
'''
from random import randint
c=0
for i in range(20):#діапазон чисел
    m=randint(100,200)#масив рандомних чисел
    if m%2==0:#умовні пари
        c=m+c#рекурсія
print('Сумуа парних елементів масиву цілих чисел:', c)