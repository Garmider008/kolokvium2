'''
Дан одновимірний масив цілих чисел. Знайдіть, скільки разів в ньому
повторюється найчастіше число.
Гармідер Анастасія 122В
'''
c=0
a=[1223,43546,455,554,453]
for b in range(len(a)):
    m=a.count(a[b])#кількість зустрічей певного ітеріруемого об'єкта
    if m>c:#якщо ця кількість більше того, що був раніше, то перезаписуємо найбільший
        c=m
print('Стільки разів повторюється найчастіше число:',c)