'''
Якщо в одновимірному масиві є три поспіль однакових елемента, то
змінній r привласнити значення істина.
Гармідер Анастасія 122В
'''
r=False
t=[1123,6563,5645,56,54,3,3,3,65463,1123]
for c in range(len(t)-2):#діапазон без 2 останніх так як виходить з діапазону
    if t[c]==t[c+1]==t[c+2]:#перевірка
        r=True
print('Змінна набуває значення:',r)