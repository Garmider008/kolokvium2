'''
Перетин даху має форму півкола з радіусом R м. Сформувати таблицю,
яка містить довжини опор, які встановлюються через кожні R / 5 м.
Гармідер Анастасія 122В
'''
import math
a=int(input('Введіть радіус даху:'))
b=(a//5)
d=a/5#задаєм інтервал
for i in range(b):
    e=a*a#формула Піфагора
    d=d+(a/5)
    f=d*d
    g=math.sqrt(e-f)
    print(g)
