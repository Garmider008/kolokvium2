'''
Знайти добуток елементів лінійного масиву цілих чисел, які кратні 5.
Розмірність масиву - 10. Заповнення масиву здійснити випадковими числами від 10 до
100.
Гармідер Анастасія 122В
'''
from random import randint
c=1
for i in range(30):#діапазон
    o=randint(10,100)#рандом
    if o%5==0:#умова
        c=c*o#створення
if c==1:
    print('Таких чисел немає')
else:
    print('Добуток елементів лінійного масиву цілих чисел, які кратні 5: ',c)