'''
Знайти добуток елементів масиву, кратних 3 і 9. Розмірність масиву - 10.
Заповнення масиву здійснити випадковими числами від 5 до 500.
Гармідер Анастасія 122В
'''
from random import randint
a=1
for i in range(10):#діапазон
    b=randint(5,500)#рандом
    if b%3==0 and b>1:#умова, яка говорить за діаграмою Ейлера -Вення , що 3 є під множиною 9
        a=a*b
if a==1:
    print('Таких чисел немає')
else:
    print('Добуток елементів масиву, кратних 3 і 9:', a)