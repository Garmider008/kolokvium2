'''
У лінійному масиві знайти максимальний елемент. Вставте порядковий
номер елемента за ним, пересунувши всі залишилися на одну позицію вправо.
Гармідер Анастасія 122В
'''
a=[1,2,3,4,5,6,200,1,2]
l=list()
b=max(a)#шукаємо максимальний елемент
for i in range(len(a)):#прохидомось по масиву
    l.append(a[i])#заносимо до іншого масиву
    if a[i]==b:#якщо умова винолась то ставимо після максимального
        l.append(i)#добаляємо всьо сюди
print(l)