'''
Створіть масив з 15 цілочисельних елементів і визначте серед них
мінімальне значення.
Гармідер Анпстасія
'''
from random import randint
c=1000
for i in range(15):#діапазон масиву
    b=randint(-999,999)#Масив
    if b<c:# перевіряємо чи менше інтеріюєме ніж попереднє мінімальне
        c=b#робимо його мінімальним
print(c)