'''
Знайти кількість парних елементів одновимірного масиву до першого
зустрінутого числа рівного наперед заданому числу а.
Гармідер Анастасія 122В
'''
from random import randint
a=24
d=int(input('Введіть число:'))
for i in range(5):#діапазон
    g=randint(10,20)#масив
for i in range(10):#діапазон
    g=randint(1,15)
    if g%2==0:#умова
        a+=1#лічильник
    if g==d:
        break
print('Кількість парних елементів',a)


