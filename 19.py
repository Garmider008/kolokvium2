'''
Знайти суму всіх елементів масиву цілих чисел що задовольняють умові:
остача від ділення на 2 дорівнює 3. Розмірність масиву - 20. Заповнення масиву
здійснити випадковими числами від 200 до 300.
Гармідер Анастасія 122В
'''
from random import randint
c=0
for i in range(20):#діапазон
    u=randint(200,300)#масив
    if u%2==3:#умова, але остача від ділення на 2 завжи 1 або 0
        c=c+u#сума
print('Сума всіх елементів масиву цілих чисел:', c)